package pbo2.pkg10116058.latihan2.deklarasi;


public class PBO210116058Latihan2Deklarasi {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //Deklarasi Variable
        int nilaiInt;
        final double PHI = 3.14; //konstanta uppercase
        boolean nilaiLogika;
        char nilaiKarakter;
        
        //Memberi nilai ke variable
        nilaiInt = 78;
        nilaiLogika = false;
        nilaiKarakter = 'D'; 
        
        // Menampilkan Hasil
        System.out.println();
        System.out.println("Isi Variabel Nilai =" + nilaiInt);
        System.out.println("Isi Variabel PHI =" + PHI);
        System.out.println("Isi Variabel Logika =" + nilaiLogika);
        System.out.println("Isi Variabel Karakter=" + nilaiKarakter);
        
     
    }
    
}